    public int win(int[][] board) {
        //TODO: make winning 3 green/red depending on who won or draw line through winning arrangment
        
        //Test each row for horizontal win
        for (int[] row: board) {
            if (row[0] != 0 && row[0] == row[1] && row[1] == row[2]) return row[0];
        }
        //Test each column for vertical win
        for (int i = 0; i < 3; i++) {
            if (board[0][i] != 0 && board[0][i] == board[1][i] && board[1][i] == board[2][i]) return board[0][i];
        }
        //Test top-left -> bottom-right diagonal for win
        if (board[0][0] != 0 && board[0][0] == board[1][1] && board[1][1] == board[2][2]) return board[0][0];
        //Test top-left -> bottom-right diagonal for win
        if (board[0][2] != 0 && board[0][2] == board[1][1] && board[1][1] == board[2][0]) return board[0][2];
        //No other possible wins so return 0
        return 0;
    }
    public boolean noEmpty() {
        //Loop through each element and return false if any cell is empty
        for (int[] row: board) {
            for (int i: row) {
                if (i == 0) return false;
            }
        }
        //Otherwise return true
        return true;
    }
    public void update() {
        boolean done = false;
        //Update visible board
        updateText();
        //Since only player can win on their turn, check if they won
        if (win(deepCopy(board)) == 1) {
            done = true;
            //Show winning screen since player won
            jLabel11.setText("You won.");
            jDialog1.setVisible(true);
        }
        //If no empty, show tie screen (since neither player won)
        else if (noEmpty()) {
            //Show the tie screen
            jDialog1.setVisible(true);
        }
        //Make it opponent's turn
        turn = 2;
        
        //Sleep to make it look like AI is thinking
        //TODO: Threads
//        try {
//            Thread.sleep(1000);
//        } catch (InterruptedException ex) {
//            Logger.getLogger(Board.class.getName()).log(Level.SEVERE, null, ex);
//        }
        
        //If game is still going
        if (!done) {
            //Do opponent move
            int[] coords = good_ai_move(deepCopy(board));
            board[coords[0]][coords[1]] = 2;
            //Update visible board after AI move
            updateText();
            if (win(deepCopy(board)) == 2) {
                done = true;
                //Show losing screen since opponent won
                jLabel11.setText("You lost.");
                jDialog1.setVisible(true);
            }
            //Make it player's turn
            turn = 1;
        }
    }
    public void updateText() {
        //Update label text
        JLabel[] labels = {jLabel1, jLabel2, jLabel3, jLabel4, jLabel5, jLabel6, jLabel7, jLabel8, jLabel9};
        for (int i = 0; i < 9; i++) {
            String text = "" + board[i / 3][i % 3];
            if (text.equals("0")) text = "";
            if (text.equals("1")) text = "X";
            if (text.equals("2")) text = "O";
            labels[i].setText(text);
        }
    }
    public int[] bad_ai_move(int[][] board) {
        boolean found = false;
        int[] coords = new int[2];
        for (int i = 0; i < board.length; i++) {
            for (int j = 0; j < board[0].length; j++) {
                if (!found && board[i][j] == 0) {
                    coords[0] = i;
                    coords[1] = j;
                    found = true;
                }
            }
        }
        return coords;
    }
    public int[] less_bad_ai_move(int[][] board) {
        ArrayList<int[]> allCoords = new ArrayList<>();
        for (int i = 0; i < board.length; i++) {
            for (int j = 0; j < board[0].length; j++) {
                if (board[i][j] == 0) {
                    allCoords.add(new int[]{i, j});
                }
            }
        }
        Collections.shuffle(allCoords);
        return allCoords.get(0);
    }
    public int[] okay_ai_move(int[][] board) {
        for (int i = 0; i < board.length; i++) {
            for (int j = 0; j < board[0].length; j++) {
                if (board[i][j] == 0) {
                    board[i][j] = 1;
                    if (win(board) == 1) return new int[]{i, j};
                    board[i][j] = 0;
                }
            }
        }
        return less_bad_ai_move(board);
    }
    public int[] good_ai_move(int[][] board) {
        for (int i = 0; i < board.length; i++) {
            for (int j = 0; j < board[0].length; j++) {
                if (board[i][j] == 0) {
                    board[i][j] = 2;
                    if (win(board) == 2) return new int[]{i, j};
                    board[i][j] = 0;
                }
            }
        }
        for (int i = 0; i < board.length; i++) {
            for (int j = 0; j < board[0].length; j++) {
                if (board[i][j] == 0) {
                    board[i][j] = 1;
                    if (win(board) == 1) return new int[]{i, j};
                    board[i][j] = 0;
                }
            }
        }
        return less_bad_ai_move(board);
    }
